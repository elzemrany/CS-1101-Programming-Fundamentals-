# Function to compare two numbers

#

def compare(a, b):

    if a < b:

        return -1

    elif a > b:

        return 1

    else:

        return 0

#

# a < b condition

retvalue = compare(2, 5)

print (retvalue)

#

# a > b condition

retvalue = compare(5, 2)

print (retvalue)

#

# a == b condition

retvalue = compare(3, 3)

print (retvalue)

